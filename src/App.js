import React, { useState, useEffect } from 'react';
import { motion } from 'framer-motion';

function App() {
  const [audio, setAudio] = useState(bankTwo);
  const [display, setDisplay] = useState('');

  return (
    <div id="drum-machine" className="">
      <PadBank audio={audio} display={display} setDisplay={setDisplay} />
      <p id="display" />
    </div>
  );
}

const DrumPad = ({ id, url, keyCode, keyTrigger, setDisplay }) => {
  const audio = new Audio(url);
  const key = useKeyPress(keyTrigger.toLowerCase());

  const playSound = () => {
    audio.currentTime = 0;
    audio.play();
    setDisplay(id);
  };

  useEffect(() => {
    function handleKeyPress(e) {
      // console.log(e);
      e.keyCode === keyCode && playSound();
    }

    window.addEventListener('keyup', handleKeyPress);

    return () => {
      window.removeEventListener('keyup', handleKeyPress);
    };
  }, []);

  return (
    <motion.div
      id={keyCode}
      className="drum-pad"
      onClick={playSound}
      whileTap={{
        scale: 1.2,
        borderColor: '#ffc600',
        boxShadow: '0 0 1rem #ffc600'
      }}
    >
      <audio className="clip" data-key={keyCode} src={audio} id={keyTrigger} />
      <kbd className="key">{keyTrigger}</kbd>
    </motion.div>
  );
};

const PadBank = ({ audio, display, setDisplay }) => {
  return (
    <div className="pad-bank-wrapper">
      <div className="pad-bank">
        {audio.map((item, i) => (
          <DrumPad
            id={item.id}
            url={item.url}
            keyCode={item.keyCode}
            keyTrigger={item.keyTrigger}
            key={i}
            setDisplay={setDisplay}
          />
        ))}
      </div>
      <div id="display" className="display">
        {display}
      </div>
    </div>
  );
};

function useKeyPress(targetKey) {
  // State for keeping track of whether key is pressed
  const [keyPressed, setKeyPressed] = useState(false);
  // If pressed key is our target key then set to true
  function downHandler({ key }) {
    if (key === targetKey) {
      setKeyPressed(true);
    }
  }
  // If released key is our target key then set to false
  const upHandler = ({ key }) => {
    if (key === targetKey) {
      setKeyPressed(false);
    }
  };
  // Add event listeners
  useEffect(() => {
    window.addEventListener('keydown', downHandler);
    window.addEventListener('keyup', upHandler);
    // Remove event listeners on cleanup
    return () => {
      window.removeEventListener('keydown', downHandler);
      window.removeEventListener('keyup', upHandler);
    };
  }, []); // Empty array ensures that effect is only run on mount and unmount
  return keyPressed ? targetKey : '';
}

